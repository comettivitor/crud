    public void salvaDadosDatabase() {
        cliente.setNomeCliente(jTextField1.getText().toUpperCase());
        cliente.setEnderecoCliente(jTextField2.getText().toUpperCase());
        cliente.setBairroCliente(jTextField3.getText().toUpperCase());
        cliente.setNumCliente(jTextField4.getText().toUpperCase());
        cliente.setCepCliente(jTextField5.getText().toUpperCase());
        cliente.setCidadeCliente(jTextField6.getText().toUpperCase());
        cliente.setUfCliente(jTextField7.getText().toUpperCase());
        clientedao.save(cliente);
    }

    public void updateCliente() {
        cliente.setIdCliente(jLabel10.getText().toUpperCase());
        cliente.setNomeCliente(jTextField1.getText().toUpperCase());
        cliente.setEnderecoCliente(jTextField2.getText().toUpperCase());
        cliente.setBairroCliente(jTextField3.getText().toUpperCase());
        cliente.setNumCliente(jTextField4.getText().toUpperCase());
        cliente.setCepCliente(jTextField5.getText().toUpperCase());
        cliente.setCidadeCliente(jTextField6.getText().toUpperCase());
        cliente.setUfCliente(jTextField7.getText().toUpperCase());
        clientedao.alterar(cliente);
    }

    public void deletarCliente() {
        cliente.setIdCliente(jLabel10.getText().toUpperCase());
        clientedao.deletar(cliente);
    }

    public void buscarClientes() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        ((DefaultTableModel) modelo).setRowCount(0);
        for (Cliente c : clientedao.listarClientes()) {
            modelo.addRow(new Object[]{
                c.getIdCliente(),
                c.getNomeCliente(),
                c.getEnderecoCliente(),
                c.getBairroCliente(),
                c.getNumCliente(),
                c.getCepCliente(),
                c.getCidadeCliente(),
                c.getUfCliente()
            });
        }
    }

