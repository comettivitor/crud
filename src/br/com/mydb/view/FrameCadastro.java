    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            String nome = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
            String endereco = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();
            String bairro = jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString();
            String num = jTable1.getValueAt(jTable1.getSelectedRow(), 4).toString();
            String cep = jTable1.getValueAt(jTable1.getSelectedRow(), 5).toString();
            String cidade = jTable1.getValueAt(jTable1.getSelectedRow(), 6).toString();
            String uf = jTable1.getValueAt(jTable1.getSelectedRow(), 7).toString();
            String id = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();

            jLabel10.setText(id);
            jTextField1.setText(nome);
            jTextField2.setText(endereco);
            jTextField3.setText(bairro);
            jTextField4.setText(num);
            jTextField5.setText(cep);
            jTextField6.setText(cidade);
            jTextField7.setText(uf);
        }
    }//GEN-LAST:event_jTable1MouseClicked

