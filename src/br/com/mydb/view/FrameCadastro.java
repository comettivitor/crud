    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        DefaultTableModel filtro = (DefaultTableModel) jTable1.getModel();
        String procura = jTextField1.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(filtro);
        jTable1.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(procura));
        
        jTextField1.setText(jTextField1.getText().toUpperCase());
    }//GEN-LAST:event_jTextField1KeyReleased

